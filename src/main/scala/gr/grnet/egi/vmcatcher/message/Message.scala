package gr.grnet.egi.vmcatcher.message

/**
 *
 * @author Christos KK Loverdos <loverdos@gmail.com>
 */
sealed trait Message

// JSON as it is generated by the vmcatcher (python) script
case class VmCatcherScriptJSON(json: String) extends Message

// JSON describing an image as it appears in an image list
case class ImageJSON(imageConfig: ImageConfig) extends Message

case class UnparsedMessage(error: String) extends Message

/**
 *
 * @author Christos KK Loverdos <loverdos@gmail.com>
 */
object Message {
  def parseJson(json: String): Message = {
    try {
      val imageConfig = ImageConfig.ofString(json)
      val isImageJSON = imageConfig.hasDcIdentifier && imageConfig.hasHvURI
      if(isImageJSON) {
        ImageJSON(imageConfig)
      } else {
        VmCatcherScriptJSON(json)
      }
    }
    catch {
      case e: Exception â‡’
        UnparsedMessage(e.toString)
    }
  }
}
